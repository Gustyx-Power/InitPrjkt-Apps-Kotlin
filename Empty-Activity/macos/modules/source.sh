EOF

    # themes.xml
    if [[ "$USE_COMPOSE" == "y" ]]; then
        cat <<EOF > "$FULL_PROJECT_PATH/app/src/main/res/values/themes.xml"
<?xml version="1.0" encoding="utf-8"?>
<resources>
    <style name="Theme.$PROJECT_NAME" parent="android:Theme.Material.Light.NoActionBar" />
</resources>
