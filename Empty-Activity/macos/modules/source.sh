#!/bin/bash
# Source Module
# Handles source code and resource files creation

create_source_files() {
    echo "Creating source files..."
    
    # Create MainActivity.kt
    if [[ "$USE_COMPOSE" == "y" ]]; then
        create_compose_main_activity
        create_compose_theme_files
    else
        create_view_main_activity
        create_activity_main_xml
    fi
    
    create_resource_files
    create_test_files
}

create_compose_main_activity() {
    cat <<EOF > "$FULL_PROJECT_PATH/app/src/main/java/$PACKAGE_PATH/MainActivity.kt"
package $PACKAGE

import android.os.Bundle
import androidx.activity.ComponentActivity
import androidx.activity.compose.setContent
import androidx.activity.enableEdgeToEdge
import androidx.compose.foundation.layout.fillMaxSize
import androidx.compose.foundation.layout.padding
import androidx.compose.material3.Scaffold
import androidx.compose.material3.Text
import androidx.compose.runtime.Composable
import androidx.compose.ui.Modifier
import androidx.compose.ui.tooling.preview.Preview
import $PACKAGE.ui.theme.${PROJECT_NAME}Theme

class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        enableEdgeToEdge()
        setContent {
            ${PROJECT_NAME}Theme {
                Scaffold(modifier = Modifier.fillMaxSize()) { innerPadding ->
                    Greeting(
                        name = "Android",
                        modifier = Modifier.padding(innerPadding)
                    )
                }
            }
        }
    }
}

@Composable
fun Greeting(name: String, modifier: Modifier = Modifier) {
    Text(
        text = "Hello \$name!",
        modifier = modifier
    )
}

@Preview(showBackground = true)
@Composable
fun GreetingPreview() {
    ${PROJECT_NAME}Theme {
        Greeting("Android")
    }
}
EOF
}

